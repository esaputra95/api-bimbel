import Model from "#root/services/PrismaService";
import { Request, Response } from "express";
import { Prisma } from "@prisma/client";
import { handleValidationError } from "#root/helpers/handleValidationError";
import { errorType } from "#root/helpers/errorType";
import { MaterialQueryInterface } from "#root/interfaces/masters/MaterialInterface";

const getData = async (req:Request<{}, {}, {}, MaterialQueryInterface>, res:Response) => {
    try {
        const query = req.query;
        // PAGING
        const take:number = parseInt(query.limit ?? 20 )
        const page:number = parseInt(query.page ?? 1 );
        const skip:number = (page-1)*take
        // FILTER
        let filter:any= []
        query.name ? filter = [...filter, {name: { contains: query.name }}] : null
        if(filter.length > 0){
            filter = {
                OR: [
                    ...filter
                ]
            }
        }
        const data = await Model.materials.findMany({
            where: {
                ...filter
            },
            include: {
                courses: {
                    include: {
                        majors: {
                            include: {
                                universities: true
                            }
                        }
                    }
                }
            },
            skip: skip,
            take: take
        });
        const total = await Model.materials.count({
            where: {
                ...filter
            }
        })
        res.status(200).json({
            status: true,
            message: "successfully in getting Material data",
            data: {
                material: data,
                info:{
                    page: page,
                    limit: take,
                    total: total
                }
            }
        })
    } catch (error) {
        let message = errorType
        message.message.msg = `${error}`
        res.status(message.status).json({
            status: false,
            errors: [
                message.message
            ]
        })
    }
}

const postData = async (req:Request, res:Response) => {
    try {
        const data = { ...req.body};
        await Model.materials.create({data: data});
        res.status(200).json({
            status: true,
            message: 'successfully in created Material data'
        })
    } catch (error) {
        console.log({error});
        
        let message = errorType
        message.message.msg = `${error}`
        if (error instanceof Prisma.PrismaClientKnownRequestError) {
            message =  await handleValidationError(error)
        }
        res.status(message.status).json({
            status: false,
            errors: [
                message.message
            ]
        })
    }
}

